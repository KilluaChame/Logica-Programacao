
//Classe principal do programa,sempre deve ter o mesmo nome do arquivo.
public class Tipo_Dado_Exercicio1 {

    // Método principal do Java, ponto de entrada da aplicação.
    public static void main(String[] args) {

        /* ================ Exercício 1: Explorando Tipos Primitivos ================ */

        // Este programa demonstra o uso de tipos de dados primitivos em Java

        /* ============================ DECLARACAO ============================ */

        int numeroInteiro = 45; // Variável para armazenar o número inteiro.
        double numeroDecimal = 3.14; // Variável para armazenar o número decimal.
        char letra = 'M'; // Variável para armazenar a letra.
        String texto = "Mateus"; // Variável para armazenar o texto.
        boolean verdadeiro = true; // Variável booleana para armazenar verdadeiro ou falso.

        /* ======================== SAIDA DE DADOS ======================== */

        System.out.println("\n==================== INICIO DO PROGRAMA ====================\n");
        System.out.println("Número inteiro (int): " + numeroInteiro); // Exibe o número inteiro.
        System.out.println("Número decimal (double): " + numeroDecimal); // Exibe o número decimal.
        System.out.println("Letra (char): " + letra); // Exibe a letra.
        System.out.println("Texto (String): " + texto); // Exibe o texto.
        System.out.println("Verdadeiro (boolean): " + verdadeiro); // Exibe o valor booleano.
        System.out.println("\n==================== FIM DO PROGRAMA ====================\n\n");

    }
}